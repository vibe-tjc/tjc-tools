#!/bin/bash
# Get suggested Bible verses for a topic
# Usage: ./get-verses.sh "topic"

TOPIC="$1"

if [ -z "$TOPIC" ]; then
    echo "Usage: $0 <topic>"
    exit 1
fi

# Verse database
declare -A VERSES

VERSES["聖靈"]="
使徒行傳 2:38 - 你們各人要悔改，奉耶穌基督的名受洗，叫你們的罪得赦，就必領受所賜的聖靈。
約翰福音 3:5 - 耶穌說：我實實在在的告訴你，人若不是從水和聖靈生的，就不能進神的國。
以弗所書 1:13-14 - 你們既聽見真理的道，就是那叫你們得救的福音，也信了基督，既然信他，就受了所應許的聖靈為印記。
"

VERSES["洗禮"]="
馬可福音 16:16 - 信而受洗的，必然得救；不信的，必被定罪。
使徒行傳 2:38 - 你們各人要悔改，奉耶穌基督的名受洗，叫你們的罪得赦，就必領受所賜的聖靈。
羅馬書 6:3-4 - 豈不知我們這受洗歸入基督耶穌的人是受洗歸入他的死嗎？
"

VERSES["安息日"]="
出埃及記 20:8-11 - 當記念安息日，守為聖日。
以賽亞書 58:13-14 - 你若在安息日掉轉你的腳步，在我聖日不以操作為喜樂...
馬可福音 2:27-28 - 安息日是為人設立的，人不是為安息日設立的。
"

VERSES["禱告"]="
腓立比書 4:6-7 - 應當一無掛慮，只要凡事藉著禱告、祈求，和感謝，將你們所要的告訴神。
馬太福音 6:9-13 - 所以，你們禱告要這樣說：我們在天上的父...
帖撒羅尼迦前書 5:17 - 不住的禱告。
"

VERSES["信心"]="
希伯來書 11:1 - 信就是所望之事的實底，是未見之事的確據。
希伯來書 11:6 - 人非有信，就不能得神的喜悅；因為到神面前來的人必須信有神，且信他賞賜那尋求他的人。
羅馬書 10:17 - 可見信道是從聽道來的，聽道是從基督的話來的。
"

VERSES["愛心"]="
哥林多前書 13:4-7 - 愛是恆久忍耐，又有恩慈；愛是不嫉妒；愛是不自誇，不張狂...
約翰福音 3:16 - 神愛世人，甚至將他的獨生子賜給他們，叫一切信他的，不至滅亡，反得永生。
約翰一書 4:8 - 沒有愛心的，就不認識神，因為神就是愛。
"

VERSES["救恩"]="
以弗所書 2:8-9 - 你們得救是本乎恩，也因著信；這並不是出於自己，乃是神所賜的。
羅馬書 6:23 - 因為罪的工價乃是死；惟有神的恩賜，在我們的主基督耶穌裡，乃是永生。
使徒行傳 4:12 - 除他以外，別無拯救；因為在天下人間，沒有賜下別的名，我們可以靠著得救。
"

VERSES["得救"]="
約翰福音 3:5 - 耶穌說：我實實在在的告訴你，人若不是從水和聖靈生的，就不能進神的國。
馬可福音 16:16 - 信而受洗的，必然得救；不信的，必被定罪。
使徒行傳 2:38 - 你們各人要悔改，奉耶穌基督的名受洗，叫你們的罪得赦，就必領受所賜的聖靈。
"

echo "主題：$TOPIC"
echo ""

if [ -n "${VERSES[$TOPIC]}" ]; then
    echo "建議經文："
    echo "${VERSES[$TOPIC]}"
else
    echo "目前沒有「$TOPIC」的預設經文建議。"
    echo ""
    echo "建議搜尋："
    echo "https://www.biblegateway.com/quicksearch/?search=$(echo "$TOPIC" | jq -sRr @uri)&version=CUVS"
fi
